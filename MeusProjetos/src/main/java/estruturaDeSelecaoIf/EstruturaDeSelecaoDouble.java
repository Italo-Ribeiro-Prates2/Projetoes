
package estruturaDeSelecaoIf;


public class EstruturaDeSelecaoDouble {
    public static void main(String[] args) {
        
        double numero1 = 0.3;
        double numero2 = 0.2 + 0.1;
        
        if(Double.compare(numero1, numero2) ==  0){
            System.out.println("Os números são aproximadamente iguais");
        
    }else{
            System.out.println("Os número são diferentes");
        }
            
        
    }
    
}
